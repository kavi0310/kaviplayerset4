#include<stdio.h>
int main()
{
    int n,a[100][100],i,j,c=0;
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        for(j=0;j<n;j++)
        {
        scanf("%d",&a[i][j]);
        }
    }
    for(i=0;i<n;i++)
    {
        for(j=0;j<n;j++)
        {
        if(i==j)
        {
            if(a[i][j]==1)
            {
            c++;
            }
        }
        }
    }
    if(n!=1)
    {
    if(a[i][n-1]==1)
    {
        c++;
    }
    if(a[n-1][0]==1)
    {
    c++;
    }
    }
    printf("%d",c);
}
